// A4 standard metrics in pixels
export const PAGE_HEIGHT_PX = PAGE_HEIGHT_INCH * CSS_PIXELS_PER_INCH;
export const PAGE_WIDTH_PX = PAGE_WIDTH_INCH * CSS_PIXELS_PER_INCH;

